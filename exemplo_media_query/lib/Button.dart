import 'package:flutter/material.dart';
import 'dart:math' as Math;
void main() {
  runApp(MaterialApp(
    home: ColorChangeButton(),
  ));
}

class ColorChangeButton extends StatefulWidget {
  @override
  _ColorChangeButtonState createState() => _ColorChangeButtonState();
}

class _ColorChangeButtonState extends State<ColorChangeButton> {
  Color _buttonColor = Colors.blue;

  void _changeButtonColor() {
    setState(() {
      // Muda a cor do botão para uma cor aleatória
      _buttonColor = Color((Math.Random().nextDouble() * 0xFFFFFF).toInt()).withOpacity(1.0);
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Botão que Muda de Cor', style: TextStyle(color: Colors.white),),
        
      ),
      body: Center(
        child: ElevatedButton(
          style: ElevatedButton.styleFrom(backgroundColor: _buttonColor), // Define a cor do botão
          onPressed: _changeButtonColor, // Define a ação que muda a cor
          child: Text('Clique para mudar a cor'),
        ),
      ),
    );
  }
}

